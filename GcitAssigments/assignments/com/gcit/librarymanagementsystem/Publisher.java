public class Publisher {
	private int publisherId;
	private String publisherName;
	private String publisherAdress;
	private int publisherNumber;
	/**
	 * @return the publisherId
	 */
	public int getPublisherId() {
		return publisherId;
	}
	/**
	 * @param publisherId the publisherId to set
	 */
	public void setPublisherId(int publisherId) {
		this.publisherId = publisherId;
	}
	/**
	 * @return the publisherName
	 */
	public String getPublisherName() {
		return publisherName;
	}
	/**
	 * @param publisherName the publisherName to set
	 */
	public void setPublisherName(String publisherName) {
		this.publisherName = publisherName;
	}
	/**
	 * @return the publisherAdress
	 */
	public String getPublisherAdress() {
		return publisherAdress;
	}
	/**
	 * @param publisherAdress the publisherAdress to set
	 */
	public void setPublisherAdress(String publisherAdress) {
		this.publisherAdress = publisherAdress;
	}
	/**
	 * @return the publisherNumber
	 */
	public int getPublisherNumber() {
		return publisherNumber;
	}
	/**
	 * @param publisherNumber the publisherNumber to set
	 */
	public void setPublisherNumber(int publisherNumber) {
		this.publisherNumber = publisherNumber;
	}

}
